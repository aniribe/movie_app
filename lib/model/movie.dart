class Movie {
  final String image;
  final String title;
  final String genre;
  final String age;
  final String rating;
  final double watchedValue;
  final int time;

  Movie({
    required this.image,
    required this.title,
    required this.genre,
    required this.age,
    required this.rating,
    required this.watchedValue,
    required this.time,
  });
}
